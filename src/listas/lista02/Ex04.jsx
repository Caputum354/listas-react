// Exercício 4 — Soma de números do array
function Ex04() {
  // Array de números para somar
  const numeros = [5, 10, 15];
  // Soma as três posições manualmente (5 + 10 + 15 = 30)
  const soma = numeros[0] + numeros[1] + numeros[2];
  // Exibe o resultado da soma
  return <p>Resultado da soma: {soma}</p>;
}
// Exporta o componente
export default Ex04;
