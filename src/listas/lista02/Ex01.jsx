// Exercício 1 — Criar um array de frutas
function Ex01() {
  // Cria um array (lista) com três frutas
  const frutas = ["Maçã", "Banana", "Uva"];
  // frutas[0] acessa a PRIMEIRA posição (arrays começam no índice 0)
  return <p>Primeira fruta: {frutas[0]}</p>;
}
// Exporta o componente
export default Ex01;
