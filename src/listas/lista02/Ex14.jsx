// Exercício 14 — Soma de três números do array
function Ex14() {
  // Array com os valores
  const valores = [2, 4, 6];
  // Soma as três posições (2 + 4 + 6 = 12)
  const resultado = valores[0] + valores[1] + valores[2];
  // Exibe o resultado
  return <p>Resultado: {resultado}</p>;
}
// Exporta o componente
export default Ex14;
