// Exercício 9 — Array de idades
function Ex09() {
  // Array com três idades
  const idades = [18, 21, 30];
  // Soma apenas as DUAS primeiras idades (18 + 21 = 39)
  const soma = idades[0] + idades[1];
  // Exibe o resultado
  return <p>A soma das duas primeiras idades é: {soma}</p>;
}
// Exporta o componente
export default Ex09;
