// Exercício 9 — Mostrar resultado de multiplicação
function Ex09() {
  // Primeiro fator
  const numero1 = 5;
  // Segundo fator
  const numero2 = 8;
  // O operador * multiplica dentro da interpolação (5 * 8 = 40)
  return <p>Multiplicação: {numero1 * numero2}</p>;
}
// Exporta o componente
export default Ex09;
